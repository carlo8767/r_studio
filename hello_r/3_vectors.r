# Creating a vector
my_vector <- c(1, 2, 3, 4, 5)

# Accessing elements
print(my_vector[1])  # First element
print(my_vector[3])  # Third element

# Adding an element
my_vector <- c(my_vector, 6)
print(my_vector)

# Arithmetic operations
vector1 <- c(1, 2, 3)
vector2 <- c(4, 5, 6)

print(vector1 + vector2)  # Addition
print(vector2 - vector1)  # Subtraction
print(2 * vector1)        # Scalar multiplication
